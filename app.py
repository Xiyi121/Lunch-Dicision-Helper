if selected_prices: filtered_df = filtered_df[filtered_df["價格"].isin(selected_prices)]
if selected_distances: filtered_df = filtered_df[filtered_df["距離"].isin(selected_distances)]
filtered_df = filtered_df[filtered_df["評價"] >= min_rating]
if exclude_types: filtered_df = filtered_df[~filtered_df["細分"].isin(exclude_types)]

