    display_df = filtered_df.rename(columns={"細分": "料理口味", "價格": "價格級別", "距離": "距離校園"})
    st.dataframe(display_df[["名稱", "類型", "料理口味", "價格級別", "距離校園", "評價"]].sort_values(by="評價", ascending=False), use_container_width=True, hide_index=True)
