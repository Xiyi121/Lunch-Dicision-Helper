price_mapping = {"$ (100元以下)": "$", "$$ (100-200元)": "$$", "$$$ (200-400元)": "$$$"}
selected_price_labels = st.sidebar.multiselect("2. 價格範圍（可多選）", options=list(price_mapping.keys()), default=list(price_mapping.keys()))
selected_prices = [price_mapping[label] for label in selected_price_labels]

distance_mapping = {"近 (步行5分內)": "近", "中 (步行5-12分)": "中", "遠 (需騎車/稍遠)": "遠"}
selected_dist_labels = st.sidebar.multiselect("3. 距離校園位置（可多選）", options=list(distance_mapping.keys()), default=list(distance_mapping.keys()))
